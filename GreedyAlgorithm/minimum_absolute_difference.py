def minimumAbsoluteDifference(arr):
    # Write your code here
    li = sorted(arr)#(list(arr))
    diff_list = []
    for i in range(0, len(li)-1):
        diff_list.append( abs(li[i]- li[i+1]) )
    return min(diff_list)    

